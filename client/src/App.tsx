import { useEffect, useState } from "react";
import "./AppStyle.css";

function App() {
  const [message, setMessage] = useState("");

  useEffect(() => {
    // 프록시 설정 덕분에 /api/... 로 바로 호출 가능
    fetch("/api/data")
      .then((res) => res.json())
      .then((data) => setMessage(data.message))
      .catch((err) => console.error(err));
  }, []);

  return (
    <div className="container">
      <h3>Vite + Express 연동 테스트 / Link test</h3>
      <p>서버메세지/Server Message: {message}</p>
    </div>
  );
}

export default App;

